        	<h2>History and Restructuring</h2>
			<p>INSOL India was conceived on September 27,1997 at a get together of lawyers and judges hosted by Arun Jaitley, Senior Advocate, presently, Union Minister of Finance, Defence and Corporate Affairs at the instance of Justice D.P. Wadhwa, the then Judge, Supreme Court of India. Justice Wadhwa was the only member of INSOL International from India at that time. A committee comprising of Arun Jaitley and Sumant Batra drafted the Charter of INSOL India setting down, amongst others, the aims and objectives of INSOL India, the categories and eligibility criteria of membership. Justice Manmohan Sarin, then a Judge of High Court of Delhi was unanimously nominated as the first President of INSOL India and Sumant Batra as the Founder Secretary of INSOL India.</p>
           
            <p>The task of negotiating the terms of membership of INSOL India with INSOL International and the use of word `INSOL' in the name of the association was successfully achieved under the leadership of Justice Wadhwa and Justice Sarin supported by Mr. Sumant Batra. The matter was taken up with the then President of INSOL International, Mr. R. Gordon Marantz, Q.C. Later on Mr. Richard C Turton, Past President of INSOL International and Miss Claire Broughton, Executive Director of INSOL International met Mr. Sumant Batra in 1998 to discuss the various modalities of membership and how the two Associations could be of mutual benefit to each other. Justice Wadhwa and Justice Sarin discussed the matter further with them at Auckland conference. INSOL India finally became the member of INSOL International in August 1999.</p>
            
